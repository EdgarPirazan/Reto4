    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String documento = txtDocument.getText();
        String queryIdEmpleado = "SELECT idEmpleado from empleado where documento = '"+documento+"';";
        try {
            connection = conexion.getConnection();
            st = connection.createStatement();
            rs = st.executeQuery(queryIdEmpleado);
            while(rs.next()){
                int idEmp = rs.getInt("idEmpleado");
                String nombres = txtName.getText();
                String apellidos = txtLastName.getText();
                String correo = txtEmail.getText();
                if (nombres.isEmpty()) {
                    JOptionPane.showMessageDialog(this,"El nombre es requerido","",JOptionPane.WARNING_MESSAGE);
                }else if (apellidos.isEmpty()) {
                    JOptionPane.showMessageDialog(this,"El apellido es requerido","",JOptionPane.WARNING_MESSAGE);
                }else if (correo.isEmpty()) {
                    JOptionPane.showMessageDialog(this,"El correo es requerido","",JOptionPane.WARNING_MESSAGE);
                }else{
                    String query = "UPDATE `empleado` SET `nombreEmpleado`='"+nombres+"',`apellidos`='"+apellidos+"',`correo`='"+correo+"' WHERE idEmpleado= "+idEmp+";";
                    
                    try {
                        connection = conexion.getConnection();
                        st=connection.createStatement();
                        st.executeUpdate(query);
                        JOptionPane.showMessageDialog(this,"El empleado se ha actualizado");
                        this.dispose();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this,"No se pudo actualizar el usuario");
                    }
                }
            }
        } catch (Exception e) {
        }
       
    }//GEN-LAST:event_btnEditarActionPerformed

